function getVariant(variants: string[], seed: number): string {
    const index = Math.abs(seed) % variants.length;
    return variants[index];
}

export function composePsychologicalAnalysis(content: ContextContent, options: ComposeOptions, contextId: string): string {
    const { energy, energyName, theme } = options;
    const seed = energy + contextId.length; 

    let text = "";

    // 1. ГЛУБОКОЕ ВВЕДЕНИЕ (200-400 символов)
    const introTemplates = SECTION_INTRO_TEMPLATES[contextId] || SECTION_INTRO_TEMPLATES['personality'];
    text += getVariant(introTemplates, seed).replace(/{energyName}/g, energyName).replace(/{theme}/g, theme);
    text += "\n\n";

    // 2. ЯДРО (Атомы из JSON + расширение)
    text += "### Психологический профиль\n";
    text += content.intro?.[0] || "";
    text += getVariant(ELABORATION_BLOCKS, seed);

    // 3. СИЛЬНЫЕ СТОРОНЫ (Развернуто)
    if (content.strengths && content.strengths.length > 0) {
        text += "### Ваши истинные ресурсы и таланты\n";
        text += "Ваша сила — это не случайный набор качеств, а отточенный механизм взаимодействия с миром. В ресурсном состоянии это проявляется следующим образом:\n\n";
        content.strengths.forEach((s, i) => {
            text += `• ${s} Это качество дает вам возможность действовать на опережение и видеть возможности там, где другие видят препятствия. В долгосрочной перспективе именно это станет фундаментом вашего масштабирования.\n\n`;
        });
    }

    // 4. ТЕНЕВЫЕ ЛОВУШКИ (Важный блок для объема и ценности)
    if (content.shadow && content.shadow.length > 0) {
        text += "### Теневые аспекты и зоны риска\n";
        text += "Каждая энергия имеет свою 'обратную сторону'. Если вы чувствуете упадок сил или повторяющиеся неудачи, скорее всего, вы попали в одну из следующих ловушек:\n\n";
        content.shadow.forEach((s, i) => {
            text += `• ${s} Важно не бороться с этим проявлением, а осознать его корень. Обычно это сигнал о том, что вы заблокировали свой творческий потенциал или действуете из страха, а не из любви.\n\n`;
        });
        text += "Эти теневые сценарии — ваши лучшие учителя. Они показывают, где именно происходит утечка жизненной энергии.\n\n";
    }

    // 5. ПОВСЕДНЕВНАЯ РЕАЛИЗАЦИЯ
    if (content.manifestation && content.manifestation.length > 0) {
        text += "### Проявление в повседневной жизни\n";
        text += "На бытовом уровне ваша настройка диктует определенный стиль жизни:\n\n";
        text += content.manifestation.join(" ") + " Именно через такие мелочи формируется ваш большой жизненный путь. Обращайте внимание на то, как вы реагируете на изменения — в этом скрыта ваша истинная природа.\n\n";
    }

    // 6. ПЕРСОНАЛЬНАЯ СТРАТЕГИЯ РОСТА (Advice)
    if (content.advice && content.advice.length > 0) {
        text += "### Рекомендации по выводу энергии в 'плюс'\n";
        text += "Для того чтобы совершить качественный скачок, вам рекомендуется придерживаться следующей стратегии:\n\n";
        content.advice.forEach(a => {
            text += `• ${a} Помните, что трансформация — это не разовое действие, а путь. Начните внедрять эти принципы постепенно, и вы заметите, как меняется пространство вокруг вас.\n\n`;
        });
    }

    // 7. ФИНАЛЬНЫЙ ИНСАЙТ
    text += "### Ключевое осознание\n";
    text += "Ваш путь по энергии " + energyName + " — это путь обретения целостности через " + theme + ". Когда вы принимаете обе стороны своей медали, вы становитесь неуязвимы для внешних кризисов.";

    return text;
}

export function generatePaywallPreview(fullText: string): string {
    const paragraphs = fullText.split("\n\n");
    const cutIndex = Math.max(3, Math.floor(paragraphs.length * 0.25));
    return paragraphs.slice(0, cutIndex).join("\n\n") + "\n\n...[ПОЛНАЯ ВЕРСИЯ ДОСТУПНА ПОСЛЕ РАЗБЛОКИРОВКИ]";
}

export function generateFullTeaser(content: ContextContent): string {
    return (content.teaser && content.teaser[0]) || "Ваш персональный код содержит ответы на фундаментальные вопросы.";
}
